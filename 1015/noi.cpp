#include<iostream>
using namespace std;

int main()
{
	int a;
	cin>>a;
	if (a==1)
	cout<<"Monday\n";
	else if (a==2)
	cout<<"Tuesday\n";
	else if (a==3)
	cout<<"Wednesday\n";
	else if (a==4)
	cout<<"Thursday\n";
	else if (a==5)
	cout<<"Friday\n";
	else if (a==6)
	cout<<"Saturday\n";
	else
	cout<<"Sunday\n";
	return 0;
}
